// Logging to console
console.log("Hello World"); // String
console.log(123); // Number-Integer
console.log(true); // Boolean
var greeting = "Hello";
console.log(greeting); // Variable
console.log([1,2,3,4,5]); // Array
console.log({a:1, b:2, c:3}); // Object, Object literal
console.table({a:1, b:2, c:3}); // Table of object
console.error("This is some error"); // Error messages
console.clear(); // Clears console messages
console.warn("This is a warning"); // Warning messages
console.time("Hello"); // Time logging, runs logs below and returns time taken to complete logging
    console.log("hello world");
    console.log("hello world");
    console.log("hello world");
    console.log("hello world");
console.timeEnd("Hello"); // End of time logging


